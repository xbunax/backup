function dz=xiaosais(t,z)
m=0.25;%碗质量/kg
d=0.01;%小孔直径/m
D=0.11;%碗底直径/m
s1=1;%缸底面积/m^2
H=0.1;%缸内原始水高/m


g=9.8;%kg.m/s^2
s=pi*D^2/4;%碗底面积
A=pi*d^2/4;%小孔面积
rou=10^3;%水密度
C=0.08;%水阻力系数
k=0.5*C*rou;%k=1/2Crou




dz=zeros(5,1);
dz(1)=z(2);
%z(3)=h;
a=H-z(3)+((s-s1)*s^2/(2*g*s1*A^2))*dz(3)^2;
b=(s*a+s*z(3)-s1*H)/(s-s1);
z(1)=b-a;
z(4)=b;
dz(4)=z(5);
dz(3)=(A/s)*(2*g*(z(1)-z(3)))^0.5;
dz(2)=dz(5)-(m*g-(k*dz(1)^2*(s-A))-rou*g*s*(z(1)-z(3)))/(m-0.5*rou*s*z(1));
